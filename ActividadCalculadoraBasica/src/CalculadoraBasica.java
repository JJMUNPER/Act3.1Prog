public class CalculadoraBasica {

    Integer suma (Integer numA, Integer numB){
        return numA + numB;
    }

    Integer resta (Integer numA, Integer numB){
        return numA - numB;
    }

    Integer multiplicacion (Integer numA, Integer numB){
        return numA * numB;
    }

    Integer division (Integer numA, Integer numB){
        return numA / numB;
    }
}
